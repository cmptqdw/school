#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int	i;
	int	y;

	i = ft_strlen(s1);
	y = 0;
	while (s2[y] && n-- > 0)
		s1[i++] = s2[y++];
	s1[i] = '\0';
	return (s1);
}
