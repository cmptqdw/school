#include "libft.h"

int		ft_premiersuiv(int n)
{
	int	d;

	d = n;
	if (!(n % 2))
		d++;
	if (n < 2)
		return (2);
	while (d < 2147483647)
	{
		if (ft_premier(d))
			return (d);
		d = d + 2;
	}
	return (2147483647);
}
