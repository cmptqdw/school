#include "libft.h"

void	ft_putnbr(int n)
{
	int	i;
	int	t;
	int neg;

	i = 1;
	t = n;
	neg = 1;
	if (n < 0)
	{
		ft_putchar('-');
		neg = -1;
	}
	while ((t = t / 10))
		i = i * 10;
	while (i)
	{
		ft_putchar(n / i * neg + '0');
		n = n % i;
		i = i / 10;
	}
}
